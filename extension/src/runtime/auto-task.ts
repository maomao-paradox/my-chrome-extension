// 顺序执行循环，确保每次操作都完成后再进行下一次
async function executeTasks(args: ExecuteTasksArgs) {
    for (let i = 0; i < 360; i++) {
        try {
            // publish 使用固定日期，editSubmitTime 使用更新后的日期
            const taskCode = await publishTask();
            console.log(`发布任务 ${taskCode}`);

            // 可选：添加延迟，避免请求过于频繁
            await new Promise(resolve => setTimeout(resolve, 500));
        } catch (error) {
            console.error(`发布任务时出错：`, error);
            // 可选：出错后继续执行下一次任务
        }
    }

    let lastPage = false;
    // let curpage = 1;

    while (!lastPage) {
        const tasks = await queryJobs();
        if (tasks.length) {
            for (const task of tasks) {
                const currentTaskDate = getCurrentDateString();

                console.log(`执行任务，日期：${currentTaskDate}`);

                // 手动增加日期，因为我们已经获取了当前日期
                addOneDay();
                const jobCode = task.job_code;
                await submitTask(jobCode);
                console.log(`提交任务 ${jobCode}`);
                await editSubmitTime(jobCode, currentTaskDate);
                console.log(`编辑提交时间 ${jobCode} 为 ${currentTaskDate}`);
                // 可选：添加延迟，避免请求过于频繁
                await new Promise(resolve => setTimeout(resolve, 500));
            }
        } else {
            lastPage = true;
        }
    }

    console.log(`所有任务执行完成`);
}


